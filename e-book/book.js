document.getElementById("coursesOnClick").onclick = ()=>{
    if (document.getElementById("courseslist").style.display == "none"){
        document.getElementById("courseslist").style.display = "flex" 
    }else{
        document.getElementById("courseslist").style.display = "none" 
    }
}
